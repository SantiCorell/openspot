export function jitterCoordsForIne(ineCode: string): { lat: number; lng: number } {
  const prov = ineCode.slice(0, 2);
  const [la, lo] = PROVINCE_ANCHORS[prov] ?? [40.2, -3.7];
  const n = parseInt(ineCode.slice(2), 10) || 1;
  return {
    lat: la + ((n % 19) - 9) * 0.018,
    lng: lo + ((n % 23) - 11) * 0.018,
  };
}
